#include<iostream>
using namespace std;
int main()
{
    int a=4,b=5;
    cout<<"the value of a+b : "<<a+b<<endl;
    cout<<"the value of a-b : "<<a-b<<endl;
    cout<<"the value of a*b : "<<a*b<<endl;
    cout<<"the value of a/b : "<<a/b<<endl;
    cout<<"the value of a%b : "<<a%b<<endl;
    cout<<"the value of a++ : "<<a++<<endl;
    cout<<"the value of a-- : "<<a--<<endl;
    cout<<"the value of ++a : "<<++a<<endl;
    cout<<"the value of --a : "<<--a<<endl;
    return 0;
}